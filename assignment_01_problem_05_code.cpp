#include <iostream>
using namespace std;
int main()
{
long long X,Y;
cin>>X;
cin>>Y;
long long sum =X+Y;
long long mul =X*Y;
long long sub =X-Y;
cout<<X<<" + "<<Y<<" = "<<sum<<endl;
cout<<X<<" * "<<Y<<" = "<<mul<<endl;
cout<<X<<" - "<<Y<<" = "<<sub<<endl;
return 0;
} 
